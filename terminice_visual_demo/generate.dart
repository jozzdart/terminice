class _TerminalEmulator {
  final List<List<_StyledRun>> _lines = <List<_StyledRun>>[<_StyledRun>[]];
  final _htmlEscape = const HtmlEscape(HtmlEscapeMode.element);

  _AnsiStyle _style = const _AnsiStyle();
  int _row = 0;
  String _bestHtml = '<pre class="terminal-pre"></pre>';
  String _bestPlainText = '';

  String get bestHtml => _bestHtml;

  String get bestPlainText => _bestPlainText;

  void write(String raw) {
    for (var i = 0; i < raw.length; i++) {
      final code = raw.codeUnitAt(i);
      if (code == 0x1B && i + 1 < raw.length) {
        if (raw.codeUnitAt(i + 1) == 0x5B) {
          final end = _findCsiEnd(raw, i + 2);
          if (end != -1) {
            _handleCsi(raw.substring(i + 2, end), raw[end]);
            i = end;
            _rememberVisibleScreen();
            continue;
          }
        }
        continue;
      }

      switch (code) {
        case 0x0A:
          _newline();
          break;
        case 0x0D:
          break;
        case 0x08:
        case 0x7F:
          break;
        default:
          if (code >= 0x20 || raw[i].trim().isNotEmpty) {
            _append(raw[i]);
          }
      }
      _rememberVisibleScreen();
    }
  }

  int _findCsiEnd(String raw, int start) {
    for (var i = start; i < raw.length; i++) {
      final code = raw.codeUnitAt(i);
      if (code >= 0x40 && code <= 0x7E) return i;
    }
    return -1;
  }

  void _handleCsi(String params, String finalChar) {
    switch (finalChar) {
      case 'm':
        _applySgr(params);
        break;
      case 'A':
        final amount = _firstInt(params) ?? 1;
        _row = math.max(0, _row - amount);
        break;
      case 'J':
        if (_row < _lines.length) {
          _lines.removeRange(_row, _lines.length);
        }
        if (_lines.isEmpty) _lines.add(<_StyledRun>[]);
        _row = math.min(_row, _lines.length - 1);
        break;
      case 'K':
        _ensureRow();
        _lines[_row].clear();
        break;
      case 'H':
      case 'f':
        _row = 0;
        _ensureRow();
        break;
      default:
        break;
    }
  }

  void _applySgr(String params) {
    final values = params.isEmpty
        ? <int>[0]
        : params.split(';').map((part) => int.tryParse(part) ?? -1).toList();

    for (var i = 0; i < values.length; i++) {
      final code = values[i];
      switch (code) {
        case 0:
          _style = const _AnsiStyle();
          break;
        case 1:
          _style = _style.copyWith(bold: true);
          break;
        case 2:
          _style = _style.copyWith(dim: true);
          break;
        case 4:
          _style = _style.copyWith(underline: true);
          break;
        case 7:
          _style = _style.copyWith(inverse: true);
          break;
        case 22:
          _style = _style.copyWith(bold: false, dim: false);
          break;
        case 24:
          _style = _style.copyWith(underline: false);
          break;
        case 27:
          _style = _style.copyWith(inverse: false);
          break;
        case 39:
          _style = _style.copyWith(clearForeground: true);
          break;
        case 49:
          _style = _style.copyWith(clearBackground: true);
          break;
        case 38:
        case 48:
          if (i + 2 < values.length && values[i + 1] == 5) {
            final color = _ansi256(values[i + 2]);
            _style = code == 38
                ? _style.copyWith(foreground: color)
                : _style.copyWith(background: color);
            i += 2;
          } else if (i + 4 < values.length && values[i + 1] == 2) {
            final color = _rgb(values[i + 2], values[i + 3], values[i + 4]);
            _style = code == 38
                ? _style.copyWith(foreground: color)
                : _style.copyWith(background: color);
            i += 4;
          }
          break;
        default:
          final color = _ansiBasic(code);
          if (color != null) {
            if ((code >= 40 && code <= 47) || (code >= 100 && code <= 107)) {
              _style = _style.copyWith(background: color);
            } else {
              _style = _style.copyWith(foreground: color);
            }
          }
      }
    }
  }

  int? _firstInt(String value) {
    final match = RegExp(r'\d+').firstMatch(value);
    return match == null ? null : int.tryParse(match.group(0)!);
  }

  void _newline() {
    _row += 1;
    _ensureRow();
  }

  void _append(String text) {
    _ensureRow();
    final line = _lines[_row];
    if (line.isNotEmpty && line.last.style == _style) {
      line.last.append(text);
      return;
    }
    line.add(_StyledRun(_style, text));
  }

  void _ensureRow() {
    while (_lines.length <= _row) {
      _lines.add(<_StyledRun>[]);
    }
  }

  void _rememberVisibleScreen() {
    final plain = _plainText().trimRight();
    if (plain.trim().isEmpty) return;
    _bestPlainText = plain;
    _bestHtml = '<pre class="terminal-pre">${_toHtml()}</pre>';
  }

  String _plainText() {
    return _lines.map((line) => line.map((run) => run.text).join()).join('\n');
  }

  String _toHtml() {
    final htmlLines = _lines.map((line) {
      return line.map((run) {
        final escaped = _htmlEscape.convert(run.text);
        final css = run.style.css;
        if (css.isEmpty) return escaped;
        return '<span style="$css">$escaped</span>';
      }).join();
    }).toList();

    while (htmlLines.isNotEmpty && htmlLines.last.trim().isEmpty) {
      htmlLines.removeLast();
    }

    return htmlLines.join('\n');
  }
}

class _StyledRun {
  final _AnsiStyle style;
  final StringBuffer _text;

  _StyledRun(this.style, String text) : _text = StringBuffer(text);

  String get text => _text.toString();

  void append(String value) {
    _text.write(value);
  }
}

class _AnsiStyle {
  final String? foreground;
  final String? background;
  final bool bold;
  final bool dim;
  final bool underline;
  final bool inverse;

  const _AnsiStyle({
    this.foreground,
    this.background,
    this.bold = false,
    this.dim = false,
    this.underline = false,
    this.inverse = false,
  });

  _AnsiStyle copyWith({
    String? foreground,
    String? background,
    bool? bold,
    bool? dim,
    bool? underline,
    bool? inverse,
    bool clearForeground = false,
    bool clearBackground = false,
  }) {
    return _AnsiStyle(
      foreground: clearForeground ? null : foreground ?? this.foreground,
      background: clearBackground ? null : background ?? this.background,
      bold: bold ?? this.bold,
      dim: dim ?? this.dim,
      underline: underline ?? this.underline,
      inverse: inverse ?? this.inverse,
    );
  }

  String get css {
    final styles = <String>[];
    var fg = foreground;
    var bg = background;

    if (inverse) {
      final previousFg = fg;
      fg = bg ?? '#08111f';
      bg = previousFg ?? 'rgba(245,247,251,0.9)';
    }

    if (fg != null) styles.add('color:$fg');
    if (bg != null) styles.add('background-color:$bg');
    if (bold) styles.add('font-weight:800');
    if (dim) styles.add('opacity:.62');
    if (underline) styles.add('text-decoration:underline');
    if (inverse) styles.add('border-radius:3px');
    if (inverse) styles.add('padding:0 1px');
    return styles.join(';');
  }

  @override
  bool operator ==(Object other) {
    return other is _AnsiStyle &&
        other.foreground == foreground &&
        other.background == background &&
        other.bold == bold &&
        other.dim == dim &&
        other.underline == underline &&
        other.inverse == inverse;
  }

  @override
  int get hashCode => Object.hash(
        foreground,
        background,
        bold,
        dim,
        underline,
        inverse,
      );
}

String? _ansiBasic(int code) {
  const normal = <int, String>{
    30: '#111827',
    31: '#ef4444',
    32: '#22c55e',
    33: '#eab308',
    34: '#3b82f6',
    35: '#d946ef',
    36: '#06b6d4',
    37: '#e5e7eb',
    90: '#6b7280',
    91: '#f87171',
    92: '#4ade80',
    93: '#facc15',
    94: '#60a5fa',
    95: '#e879f9',
    96: '#22d3ee',
    97: '#f9fafb',
  };

  if (normal.containsKey(code)) return normal[code];
  if (code >= 40 && code <= 47) return normal[code - 10];
  if (code >= 100 && code <= 107) return normal[code - 10];
  return null;
}

String _ansi256(int code) {
  if (code < 16) {
    return _ansiBasic(code < 8 ? code + 30 : code + 82) ?? '#f9fafb';
  }
  if (code >= 16 && code <= 231) {
    final index = code - 16;
    const steps = [0, 95, 135, 175, 215, 255];
    final r = steps[index ~/ 36];
    final g = steps[(index % 36) ~/ 6];
    final b = steps[index % 6];
    return _rgb(r, g, b);
  }
  if (code >= 232 && code <= 255) {
    final gray = 8 + (code - 232) * 10;
    return _rgb(gray, gray, gray);
  }
  return '#f9fafb';
}

String _rgb(int r, int g, int b) {
  String hex(int value) =>
      value.clamp(0, 255).toRadixString(16).padLeft(2, '0');
  return '#${hex(r)}${hex(g)}${hex(b)}';
}
