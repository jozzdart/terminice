typedef DemoRunner = FutureOr<void> Function(
  Terminice terminice,
  Directory sampleProject,
);

void main(List<String> args) async {
  final scriptDir = File.fromUri(Platform.script).parent;
  final outputFile =
      args.isNotEmpty ? File(args.first) : File('${scriptDir.path}/frames.js');
  final relativeSampleProject =
      Directory('terminice_visual_demo/sample_project');
  final sampleProject = relativeSampleProject.existsSync()
      ? relativeSampleProject
      : Directory('${scriptDir.path}/sample_project');
  final frames = <Map<String, Object?>>[];

  for (var componentIndex = 0;
      componentIndex < _components.length;
      componentIndex++) {
    final component = _components[componentIndex];
    for (var themeIndex = 0; themeIndex < _themes.length; themeIndex++) {
      final theme = _themes[themeIndex];
      final display =
          _displays[(componentIndex + themeIndex) % _displays.length];
      frames.add(
        await _captureFrame(
          component: component,
          theme: theme,
          display: display,
          sampleProject: sampleProject,
        ),
      );
    }
  }

  final payload = <String, Object?>{
    'kind': 'terminice-real-terminal-frames',
    'source':
        'Generated by terminice_visual_demo/generate.dart from real Terminice output.',
    'components': _components.length,
    'themes': _themes.map((theme) => theme.id).toList(growable: false),
    'frames': frames,
  };

  final json = const JsonEncoder.withIndent('  ').convert(payload);
  outputFile.writeAsStringSync(
    'globalThis.TERMINICE_REAL_DEMO = $json;\n'
    'globalThis.TERMINICE_REAL_FRAMES = globalThis.TERMINICE_REAL_DEMO.frames;\n',
  );

  stdout.writeln(
      'Generated ${frames.length} real terminal frames: ${outputFile.path}');
}

Future<Map<String, Object?>> _captureFrame({
  required DemoComponent component,
  required DemoTheme theme,
  required String display,
  required Directory sampleProject,
}) async {
  final base = _applyDisplay(_applyTheme(Terminice(), theme.id), display);
  final tester = TerminiceTester.interactive(
    base: base,
    script: _scriptForComponent(component, theme.id),
    columns: 96,
    rows: 32,
  );

  await tester.runAsync((terminice) async {
    await Future<void>.sync(() => component.run(terminice, sampleProject));
  });

  final raw = tester.output.raw;
  final screen = TerminalScreenCapture.fromAnsi(raw);

  return <String, Object?>{
    'id': component.id,
    'group': component.group,
    'summary': component.summary,
    'code': component.code,
    'theme': theme.id,
    'display': display,
    'chain': 't.${theme.id}.$display',
    'html': screen.html,
    'plainText': screen.plainText,
    'containsAnsi': tester.output.containsAnsiControls,
  };
}

TerminalScript? _scriptForComponent(DemoComponent component, String themeId) {
  if (component.id == 'themeDemo') {
    return _themeDemoScript(themeId);
  }

  return component.script;
}

TerminalScript _themeDemoScript(String themeId) {
  const themeOrder = [
    'dark',
    'matrix',
    'fire',
    'pastel',
    'ocean',
    'monochrome',
    'neon',
    'arcane',
    'phantom',
  ];
  final index = themeOrder.indexOf(themeId).clamp(0, themeOrder.length - 1);

  return TerminalScript([
    for (var i = 0; i < index; i++)
      TerminalScriptStep.key(KeyEventType.arrowDown),
    TerminalScriptStep.key(KeyEventType.esc),
  ]);
}

Terminice _applyTheme(Terminice terminice, String theme) {
  return switch (theme) {
    'dark' => terminice.dark,
    'matrix' => terminice.matrix,
    'fire' => terminice.fire,
    'pastel' => terminice.pastel,
    'ocean' => terminice.ocean,
    'monochrome' => terminice.monochrome,
    'neon' => terminice.neon,
    'arcane' => terminice.arcane,
    'phantom' => terminice.phantom,
    _ => terminice,
  };
}

String _themeIdFor(dynamic theme) {
  final colors = theme.colors as dynamic;
  final accent = colors.accent as String;
  final selection = colors.selection as String;

  if (accent == '\x1B[32m' && selection == '\x1B[32m') return 'matrix';
  if (accent == '\x1B[31m' && selection == '\x1B[31m') return 'fire';
  if (accent == '\x1B[95m' && selection == '\x1B[94m') return 'pastel';
  if (accent == '\x1B[94m' && selection == '\x1B[34m') return 'ocean';
  if (accent == '\x1B[97m' && selection == '\x1B[37m') return 'monochrome';
  if (accent == '\x1B[95m' && selection == '\x1B[93m') return 'neon';
  if (accent == '\x1B[38;5;141m' && selection == '\x1B[38;5;99m') {
    return 'arcane';
  }
  if (accent == '\x1B[38;5;103m' && selection == '\x1B[38;5;60m') {
    return 'phantom';
  }
  return 'dark';
}

Terminice _applyDisplay(Terminice terminice, String display) {
  return switch (display) {
    'compact' => terminice.compact,
    'minimal' => terminice.minimal,
    _ => terminice.verbose,
  };
}

